
public class Primitive {
	
	int i;
	double d;
	char c;
	boolean b;
	
	public Primitive(int i, double d, char c, boolean b) {
		super();
		this.i = i;
		this.d = d;
		this.c = c;
		this.b = b;
	}

	@Override
	public String toString() {
		return "Primitive [i=" + i + ", d=" + d + ", c=" + c + ", b=" + b + "]";
	}
	
	
	
	
	
	
	
	
	

}
